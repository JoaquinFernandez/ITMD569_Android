public class Movie {
	public String title;
	public String rating;
	public String synopsis;
	public String posterUrl;
	
	public Movie (String title, String rating, String synopsis, String posterUrl) {
		this.title = title;
		this.rating = rating;
		this.synopsis = synopsis;
		this.posterUrl = posterUrl;
	}
}
